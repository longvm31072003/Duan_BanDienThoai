    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        ViewModelSanPham newModelSanPham = new ViewModelSanPham();
        newModelSanPham.setNamBH(Integer.valueOf(txtNamBH.getText()));
        newModelSanPham.setGiaBan(BigDecimal.valueOf(Double.valueOf(txtGiaBan.getText())));
        newModelSanPham.setGiaNhap(BigDecimal.valueOf(Double.valueOf(txtGiaNhap.getText())));
        newModelSanPham.setSoLuongTon(Integer.valueOf(txtSoLuongTon.getText()));
        newModelSanPham.setMoTa(txtAreaMoTa.getText());
        SanPham sp = sps.getByMaSP(String.valueOf(cbbSanPham.getSelectedItem()));
        newModelSanPham.setSanPham(sp);
        MauSac ms = mss.getByMaMauSac(String.valueOf(cbbMauSac.getSelectedItem()));
        newModelSanPham.setMauSac(ms);
        DongSanPham dsp = dsps.getByMaDSP(String.valueOf(cbbDongSP.getSelectedItem()));
        newModelSanPham.setDongSanPham(dsp);
        NhaSanXuat nsx = nsxs.getByMaNSX(String.valueOf(cbbNSX.getSelectedItem()));
        newModelSanPham.setNhaSanXuat(nsx);
        ctsps.saveCTSP(newModelSanPham);
        listVMSP = ctsps.getListCTSP();
        showDataTableCTSP(listVMSP);
    }//GEN-LAST:event_btnAddActionPerformed

