    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tbList.getModel();
        dtm.setRowCount(0);

        for (QlChucVu qLChucVu : this.iChucVuService.getAll()) {
            Object[] rowData = {
                qLChucVu.getMa(),
                qLChucVu.getTen(),
                qLChucVu.getQuyenSD()== 0 ? "Đang Hoạt Động" : "Không Hoạt Động",
                qLChucVu.getLuong()

            };
            dtm.addRow(rowData);
        }
    }

