    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        List<ViewModelSanPham> listSearch = ctsps.getCTSPByNamBaoHanh(Integer.valueOf(String.valueOf(cbbTimKiem.getSelectedItem())));
        if (listSearch.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Khong ton tai san pham");
            showDataTableCTSP(listVMSP);
        } else {
            showDataTableCTSP(listSearch);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void showDataTableCTSP(List<ViewModelSanPham> lists) {
        STT = 1;
        dtm.setRowCount(0);
        for (ViewModelSanPham x : lists) {
            dtm.addRow(new Object[]{STT++, x.getSanPham().getMaSP(), x.getSanPham().getTenSP(),
                x.getNhaSanXuat().getTenNSX(), x.getMauSac().getTenMauSac(), x.getDongSanPham().getTenDongSP(),
                x.getNamBH(), x.getMoTa(), x.getSoLuongTon(), x.getGiaNhap(), x.getGiaBan()});
        }
    }

    private void loadCombobox() {

        for (SanPham sp : listSP) {
            comboSP.addElement(sp.getMaSP());
        }

        for (DongSanPham dsp : listDSP) {
            comboDSP.addElement(dsp.getMaDongSP());
        }

        for (NhaSanXuat nsx : listNSX) {
            comboNSX.addElement(nsx.getMaNSX());
        }

        for (MauSac ms : listMS) {
            comboMS.addElement(ms.getMaMauSac());
        }

        for (int i = 1990; i <= 2000; i++) {
            listNamBaoHanh.add(i);
        }
        comboNamBaoHanh.addAll(listNamBaoHanh);
        comboNamBaoHanh.setSelectedItem("1990");

    }

    private void fillData(int index) {
        ViewModelSanPham newModelSanPham = listVMSP.get(index);
        cbbSanPham.setSelectedItem(newModelSanPham.getSanPham().getMaSP());
        cbbMauSac.setSelectedItem(newModelSanPham.getMauSac().getMaMauSac());
        cbbDongSP.setSelectedItem(newModelSanPham.getDongSanPham().getMaDongSP());
        cbbNSX.setSelectedItem(newModelSanPham.getNhaSanXuat().getMaNSX());
        txtNamBH.setText(String.valueOf(newModelSanPham.getNamBH()));
        txtAreaMoTa.setText(newModelSanPham.getMoTa());
        txtGiaBan.setText(String.valueOf(newModelSanPham.getGiaBan()));
        txtGiaNhap.setText(String.valueOf(newModelSanPham.getGiaNhap()));
        txtSoLuongTon.setText(String.valueOf(newModelSanPham.getSoLuongTon()));
    }

    private void clearForm() {
        tbSanPham.clearSelection();
        txtAreaMoTa.setText("");
        txtGiaBan.setText("");
        txtGiaNhap.setText("");
        txtNamBH.setText("");
        txtSoLuongTon.setText("");
        cbbDongSP.setSelectedIndex(0);
        cbbMauSac.setSelectedIndex(0);
        cbbNSX.setSelectedIndex(0);
        cbbSanPham.setSelectedIndex(0);
    }

