    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        QLDungLuong dungLuong = new QLDungLuong();
        if(dungLuong.getTen().isEmpty()){
        JOptionPane.showMessageDialog(this, "Không được để trống tên");
        return;
        }
        dungLuong.setMa(genMa());
        dungLuong.setTen(txtTenDungLuong.getText());
        dungLuong.setTrangThai(0);

        JOptionPane.showMessageDialog(this, iDungLuongService.save(dungLuong));
        loadTable();
        clearForm();
    }//GEN-LAST:event_btnThemActionPerformed
    
    private void btnDoiTrangThaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoiTrangThaiActionPerformed
        int row = tbListDL.getSelectedRow();
        if (row <= -1) {
            JOptionPane.showMessageDialog(this, "Xin mời chọn để đổi trạng thái");
            return;
        }
        QLDungLuong dungLuong = lDungLuong.get(row);
        if(dungLuong.getTrangThai() == 0){
           dungLuong.setTrangThai(1);
        }
        else{
        dungLuong.setTrangThai(0);
        }
     
        int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn đổi trạng thái", "Đổi trạng thái", JOptionPane.YES_NO_OPTION);
        if(chon != JOptionPane.YES_OPTION){
          return;
        }
        JOptionPane.showMessageDialog(this, iDungLuongService.update(dungLuong));
        loadTable();
        clearForm();

    }//GEN-LAST:event_btnDoiTrangThaiActionPerformed

    private void tbListDLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListDLMouseClicked
       int row = tbListDL.getSelectedRow();
       if(row <= -1){
       return;
       }
       
       String ma = tbListDL.getValueAt(row, 0).toString();
       String ten = tbListDL.getValueAt(row, 1).toString();
       
       lbMa.setText(ma);
       txtTenDungLuong.setText(ten);
    }//GEN-LAST:event_tbListDLMouseClicked

