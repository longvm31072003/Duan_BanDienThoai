    private String genMa() {
        String maDL = " ";
        String s1 = "DL";
        for (int i = 1; i < lDungLuong.size() + 2; i++) {
            maDL = s1 + i;
        }
        return maDL;
    }
  private void clearForm(){
   txtTenDungLuong.setText("");
   lbMa.setText("--");
  }
